from __future__ import annotations

import argparse
from pathlib import Path

from youtube_hf_dataset.pipeline import BuildConfig, build_hf_audio_dataset


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Download YouTube audio+captions from ids.txt, split with caption timing "
            "into VTT-aligned chunks, and save a Hugging Face audio dataset."
        )
    )
    parser.add_argument(
        "--ids-txt",
        type=Path,
        default=Path("ids.txt"),
        help="Path to ids.txt (one YouTube ID or URL per line)",
    )
    parser.add_argument(
        "--work-dir",
        type=Path,
        default=Path("data"),
        help="Working directory for downloaded files and generated chunks",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("data/hf_audio_dataset"),
        help="Where to save the Hugging Face dataset (save_to_disk format)",
    )
    parser.add_argument(
        "--min-seconds",
        type=float,
        default=0.4,
        help="Minimum segment duration in seconds",
    )
    parser.add_argument(
        "--sub-langs",
        type=str,
        default="uz-orig,uz,uz-Latn,uz-Cyrl",
        help="yt-dlp subtitle language expression (default prefers Uzbek tracks)",
    )
    parser.add_argument(
        "--prefer-auto-subs",
        action="store_true",
        help="Try autogenerated captions before manually uploaded captions",
    )
    parser.add_argument(
        "--keep-intermediate",
        action="store_true",
        help="Keep downloaded audio/captions and full wav files",
    )
    parser.add_argument(
        "--overwrite-output",
        action="store_true",
        help="Delete output-dir if it already exists and is non-empty",
    )
    return parser


def main() -> None:
    parser = build_parser()
    args = parser.parse_args()

    config = BuildConfig(
        ids_txt=args.ids_txt,
        work_dir=args.work_dir,
        output_dir=args.output_dir,
        min_seconds=args.min_seconds,
        sub_langs=args.sub_langs,
        prefer_manual_subs=not args.prefer_auto_subs,
        keep_intermediate=args.keep_intermediate,
        overwrite_output=args.overwrite_output,
    )
    build_hf_audio_dataset(config)


if __name__ == "__main__":
    main()
